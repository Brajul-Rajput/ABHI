public class A11 {
    // pre/post increment/decrement
    public static void main(String[] args) {
        int a=3;
        int b=6;
        System.out.println(a);  //3
        System.out.println(a++);  //3
       System.out.println(a);  //4
        System.out.println(++a);  //5
        System.out.println(a);  //5
        System.out.println(b);   //6
        System.out.println(b--);   //6
        System.out.println(b);   // 5
        System.out.println(--b);  //4
        System.out.println(b);   //4

    }
}
